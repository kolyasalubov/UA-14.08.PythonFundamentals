# Введення значення першої змінної
змінна1 = input("Введіть значення першої змінної: ")

# Введення значення другої змінної
змінна2 = input("Введіть значення другої змінної: ")

# Виведення початкових значень змінних
print(f"Початкові значення: змінна1 = {змінна1}, змінна2 = {змінна2}")

# Обмін значеннь двох змінних без використання третьої змінної
змінна1, змінна2 = змінна2, змінна1

# Виведення змінених значень змінних
print(f"Змінені значення: змінна1 = {змінна1}, змінна2 = {змінна2}")
