a = float(input("Enter the first number a:"))
b = float(input("Enter the second number b:"))

sum=a+b
diff=a-b
prod=a*b
div=a/b
exp=a**b
floor_div=a//b
mod=a%b

print(f"a + b = {sum}")
print(f"a - b = {diff}")
print(f"a * b = {prod}")
print(f"a / b = {div}")
print(f"a ** b = {exp}")
print(f"a // b = {floor_div}")
print(f"a % b = {mod}")